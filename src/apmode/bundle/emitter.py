    def write_posterior_summary(
        self,
        candidate_id: str,
        summary_df: Any,
    ) -> Path:
        """Write a long-form posterior summary to ``{cid}_posterior_summary.parquet``.

        Expects a pandas DataFrame with the canonical column set
        ``{param, rhat, ess_bulk, ess_tail, mean, sd, q05, q50, q95}``.
        One row per parameter. Callers driving cmdstanpy should rename
        ``CmdStanMCMC.summary()`` columns (``Mean``/``StdDev``/``5%``/...)
        to match before invoking this emitter — the strict schema is the
        point, since downstream Gate-1 Bayesian checks read these fields
        by name.
        """
        import pyarrow as pa
        import pyarrow.parquet as pq

        _validate_path_component(candidate_id, "candidate_id")

        canonical = (
            "param",
            "rhat",
            "ess_bulk",
            "ess_tail",
            "mean",
            "sd",
            "q05",
            "q50",
            "q95",
        )
        actual = set(getattr(summary_df, "columns", []))
        missing = set(canonical) - actual
        if missing:
            msg = f"summary_df missing required columns: {sorted(missing)}"
            raise ValueError(msg)

        table = pa.Table.from_pandas(summary_df[list(canonical)], preserve_index=False)
        dest = self._bayesian_dir() / f"{candidate_id}_posterior_summary.parquet"
        with _DIGEST_LOCK:
            self._ensure_unsealed()
            pq.write_table(table, dest)
        return dest

    def write_posterior_draws(
        self,
        candidate_id: str,
        draws_by_param: dict[str, object],
        thin_every: int = 1,
    ) -> Path:
        """Write thinned posterior draws to ``{cid}_posterior_draws.parquet``.

        In-memory counterpart to ``copy_posterior_draws`` — used when the
        caller already has draws as numpy arrays (e.g. when tests synthesise
        draws without running cmdstanpy). The layout is long-form
        ``(chain, iter, param, value)`` so parameters can be appended over
        multiple calls without schema divergence.

        Args:
            candidate_id: Safe path component identifying the candidate.
            draws_by_param: Mapping ``param_name -> ndarray(chains, iters)``.
                Each array must have ``ndim == 2``.
            thin_every: Retain every ``thin_every``-th iteration per chain.
                Default 1 (no thinning).

        Returns:
            Absolute path to the written parquet file.
        """
        import numpy as np
        import pyarrow as pa
        import pyarrow.parquet as pq

        _validate_path_component(candidate_id, "candidate_id")
        if thin_every < 1:
            msg = f"thin_every must be >= 1, got {thin_every}"
            raise ValueError(msg)

        rows_chain: list[int] = []
        rows_iter: list[int] = []
        rows_param: list[str] = []
        rows_value: list[float] = []
        for name, arr_obj in draws_by_param.items():
            arr = np.asarray(arr_obj)
            if arr.ndim != 2:
                msg = f"draws for {name!r} must be 2-D (chains, iters); got shape {arr.shape}"
                raise ValueError(msg)
            thinned = arr[:, ::thin_every]
            n_chains, n_iters = thinned.shape
            for c in range(n_chains):
                for i in range(n_iters):
                    rows_chain.append(c)
                    rows_iter.append(i)
                    rows_param.append(name)
                    rows_value.append(float(thinned[c, i]))

        table = pa.table(
            {
                "chain": rows_chain,
                "iter": rows_iter,
                "param": rows_param,
                "value": rows_value,
            }
        )
        dest = self._bayesian_dir() / f"{candidate_id}_posterior_draws.parquet"
        with _DIGEST_LOCK:
            self._ensure_unsealed()
            pq.write_table(table, dest)
        return dest
